    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            int id = Integer.parseInt(jTextField2.getText().trim());
            String nama = jTextField4.getText().trim();

            if (nama.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama edit tidak boleh kosong.");
                return;
            }

            if (!service.update(new Mahasiswa(id, nama))) {
                JOptionPane.showMessageDialog(this, service.getLastError());
                return;
            }

            tampilkanSemuaData();
            JOptionPane.showMessageDialog(this, "Data berhasil diedit.");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID edit harus berupa angka.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            int id = Integer.parseInt(jTextField3.getText().trim());
            int pilihan = JOptionPane.showConfirmDialog(
                    this,
                    "Hapus data dengan ID " + id + "?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION
            );

            if (pilihan == JOptionPane.YES_OPTION) {
                if (!service.delete(id)) {
                    JOptionPane.showMessageDialog(this, service.getLastError());
                    return;
                }

                kosongkanFieldEditHapus();
                tampilkanSemuaData();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID hapus harus berupa angka.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int pilihan = JOptionPane.showConfirmDialog(
                this,
                "Apakah anda yakin untuk reset indeks?",
                "Konfirmasi Reset",
                JOptionPane.YES_NO_OPTION
        );

        if (pilihan == JOptionPane.YES_OPTION) {
            if (!service.indexReset()) {
                JOptionPane.showMessageDialog(this, service.getLastError());
                return;
            }

            tampilkanSemuaData();
            JOptionPane.showMessageDialog(this, "Indeks berhasil direset.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

