    private void tampilkanSemuaData() {
        idModel.clear();
        namaModel.clear();
        
        List<Mahasiswa> listMahasiswa = service.getAll();
        if (!service.getLastError().isEmpty()) {
            JOptionPane.showMessageDialog(this, service.getLastError());
            return;
        }

        for (Mahasiswa mahasiswa : listMahasiswa) {
            idModel.addElement(String.valueOf(mahasiswa.getId()));
            namaModel.addElement(mahasiswa.getNama());
        }
    }

    private void pilihData(int index) {
        if (index < 0 || index >= idModel.size()) {
            return;
        }

        jList1.setSelectedIndex(index);
        jList2.setSelectedIndex(index);
        String id = idModel.getElementAt(index);
        String nama = namaModel.getElementAt(index);
        jTextField2.setText(id);
        jTextField4.setText(nama);
        jTextField3.setText(id);
        jTextField5.setText(nama);
    }

    private void kosongkanFieldEditHapus() {
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
    }

